// fn: declare a new function
// main: usual name for the function acting as entry point to a program
// (): empty argument list
// {}: body of the function
fn main() {
    // println: print to a new line in standard output
    // !: indicates that it is a macro (not a funciton)
    // "Hello, World!": string of characters that will be printed
    println!("Hello, World!");
}